n1 = 5
n2 = 3

# n1 to the power of n2
print(n1 ** n2)

# 0001 in binary
n3 = 0x1

# Shift left by 3 bits, which is 1000 in binary or 8 in decimal
print(n3 << 3)

# Add a number to a string
print('Number: ' + str(n1))

# Numbers hold small numbers, big numbers, and complex numbers, etc.
print(5 / 3)